package Model;

public class TreeTrunkSize {
	// instance variables
	private double width;
	private double height;

	// constructor
	public TreeTrunkSize(double d, double e) {
		this.width = d;
		this.height = e;
	}

	public double getWidth() {
		return width;
	}

	public double getHeight() {
		return height;
	}
}
